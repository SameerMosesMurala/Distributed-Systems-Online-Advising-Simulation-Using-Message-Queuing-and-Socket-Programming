    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // TODO add your handling code here:
        //Notification process username
         String username = "NotificationProcess";
			if(username.length() == 0)
				return;
                        //Get the Server name
			String server = "LocalHost";
			if(server.length() == 0)
				return;
                        //get the port number 
			String portNumber = "1500";
			if(portNumber.length() == 0)
				return;
			int port = 0;
			try {
                            //Get the port number
				port = Integer.parseInt(portNumber);
			}
			catch(Exception en) {
				return;   
			}
                        //Create a new Client
			client = new Client(server, port, username,this);
			// Start the Client
			if(!client.start()) 
				return;
			connected = true;
                        //set the buttons 
                        startButton.setEnabled(false);
                        //notification polling thread start
                        new NotificationPollingThread().start();
    }                                           
