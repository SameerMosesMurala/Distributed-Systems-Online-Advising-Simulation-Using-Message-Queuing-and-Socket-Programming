    class NotificationPollingThread extends Thread {
        //public void timerMethod()
        //Method that runs forever
            public void run() {
        {
            //run until startpolling is set to true
            startpolling=true;
            //infinite loop
            while(startpolling)
            {
                try {
                    //intger to check whether the file is empty
                    int filestatus=1;
                    //open the existing file
                    File file = new File("C:\\Users\\Sameer\\Desktop\\ap.txt");
                    //buffered reader
                    BufferedReader br = new BufferedReader(new FileReader(file));
                    //if the file is empty
                    if (br.readLine() == null) {
                        filestatus=0;
                    }
                    //if the file is empty sleep the thread for 7 seconds
                    if(filestatus==0)
                    {
                        System.out.println("sleeping");
                    try {
                        //sleep the thread 
                        TimeUnit.SECONDS.sleep(7);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                    }    
                    }else{
                        //send the notifiaction message to the server 
                        System.out.println("notificationprocessmessage");
                    client.sendMessage(new ChatMessage(ChatMessage.NOTIFICATIONMESSAGE,""));
                    }
                } catch (IOException ex) {
                    Logger.getLogger(AdvisorProcess.class.getName()).log(Level.SEVERE, null, ex);
                }
                }
            
        }
        }
    }
    
